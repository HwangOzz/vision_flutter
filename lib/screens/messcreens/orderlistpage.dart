            'status': 'Í≥µÏ†ï ÏôÑÎ£å',
            'completedAt': now,
          });
          print('ÏóÖÎç∞Ïù¥Ìä∏ Îê®: ${doc.id} ‚Üí Í≥µÏ†ï ÏôÑÎ£å');

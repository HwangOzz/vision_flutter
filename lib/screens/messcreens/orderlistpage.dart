                Uri.parse("${Global.serverUrl}/set_bit"),
                headers: {"Content-Type": "application/json"},
                body: jsonEncode({"address": "M0", "value": 0}),
              );
              print('üì¥ M0 OFF ÏöîÏ≤≠ ÏôÑÎ£å: ${offResponse.body}');
            } catch (e) {
